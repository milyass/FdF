#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		s;
	char	*str;

	s = 0;
	while (s1[s] != '\0')
		s++;
	str = (char *)malloc(sizeof(*str) * (s + 1));
	if (str == NULL)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
